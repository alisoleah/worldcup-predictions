export function getFlag(countryName) {
  if (!countryName) return '';
  // Check exact match
  if (COUNTRY_FLAGS[countryName]) return COUNTRY_FLAGS[countryName];
  
  // Try case insensitive
  const key = Object.keys(COUNTRY_FLAGS).find(k => k.toLowerCase() === countryName.toLowerCase());
  if (key) return COUNTRY_FLAGS[key];
  
  return '🏳️'; // default flag
}
